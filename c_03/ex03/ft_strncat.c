char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	char	*result;

	result = dest;
	while (*dest)
		dest++;
	while (*src && nb > 0)
	{
		*dest = *src;
		dest++;
		src++;
		nb--;
	}
	*dest = '\0';
	return (result);
}
